# Arithmetic Operators
print(a+b)
print(a-b)
print(a*b) # ** can be used for exponent
print(a/b) # // for floor division

## Assignment +=,-=

#Logical AND OR NOT
print(True and True)
print(True or False)
print(not(False))

# Bitwise Operators  << left shift and right shift
print(True & True)
print(True | True)
print(~(True))
print(2<<3) # similar to ** but works on binary level (adding zero on the left)
